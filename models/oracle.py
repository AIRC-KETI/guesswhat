class OracleModel(PreTrainedModel):
    config_class = OracleConfig
    base_model_prefix = "oracle"
    supports_gradient_checkpointing = True
    _keys_to_ignore_on_load_missing = [r"position_ids"]
    
    def __init__(self, config: PretrainedConfig, *inputs, **kwargs):
        super().__init__(config, *inputs, **kwargs)

        self.clip_model = CLIPModel.from_pretrained("openai/clip-vit-large-patch14-336")
        self.fusion_model = FusionModel(OracleConfig.get_fusion_config)
    
    def forward(
        self,
        input_ids: Optional[torch.Tensor] = None,
        pixel_values: Optional[torch.FloatTensor] = None,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.Tensor] = None,
        return_loss: Optional[bool] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
    ) -> Union[Tuple, BaseModelOutputWithPooling]:
        encoder_outputs = self.clip_model(
            input_ids = input_ids,
            pixel_values = pixel_values,
            attention_mask = None,
            position_ids = position_ids,
            return_loss = return_loss,
            output_attentions = output_attentions,
            output_hidden_states = output_hidden_states,
            return_dict = return_dict,
        )
        text_embeds = encoder_outputs.text_embeds
        image_embeds = encoder_outputs.image_embeds
        return self.fusion_model(
                input_ids = torch.cat((image_embeds, text_embeds,), 1),
                attention_mask = attention_mask,
                position_ids = position_ids,
                output_attentions = output_attentions,
                output_hidden_states = output_hidden_states,
                return_dict = return_dict,
            )



